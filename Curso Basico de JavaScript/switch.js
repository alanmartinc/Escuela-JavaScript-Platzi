// Forma de validar por casos

var numero = 1;
switch (numero) {
    case 1:
        console.log("Soy un 1!");
        break;
    case 10:
        console.log("Soy un 10!");
        break;
    case 100:
        console.log("Soy un 100!");
        break;
    default:
        console.log("No soy nada");
}